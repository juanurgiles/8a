                  - Hegemon&iacute;a de trabajo  sobre el capital<br />
                  - Trabajo Asociado como  base de la producci&oacute;n y la econom&iacute;a<br />
                  - Propiedad social de los  medios de producci&oacute;n<br />
                  - Autogesti&oacute;n</p>
