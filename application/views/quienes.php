                <p class="Just">Hoy es irrefutable, que el  cooperativismo y en general el Asociativismo, es el movimiento socioecon&oacute;mico  mas grande de la humanidad; por ende, la Econom&iacute;a Solidaria es el sistema  econ&oacute;mico, social, pol&iacute;tico&nbsp; y cultural, que ha permitido ascender el  nivel de vida de millones de personas fundamentalmente&nbsp; de clase  media&nbsp; y baja, implementado en ellos valores y principios de solidaridad,  equidad y justicia. <br />
                  La econom&iacute;a solidaria se  sustenta en cinco principios esenciales:<br />
  <br />
